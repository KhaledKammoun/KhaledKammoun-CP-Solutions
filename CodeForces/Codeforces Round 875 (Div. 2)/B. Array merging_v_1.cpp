void solve() {
    int n; cin >> n;
    vector<int> arr_a(n);
    vector<int> arr_b(n);
    read_vector(arr_a,n);
    read_vector(arr_b,n);

    unordered_map<int,int> count_a_map;
    unordered_map<int,int> count_b_map;

    unordered_set<int> unique_elements;

    int longest_a = 1;
    int longest_b = 1;

    count_a_map[arr_a[0]] = 1;
    count_b_map[arr_b[0]] = 1;
    unique_elements.insert(arr_a[0]);
    unique_elements.insert(arr_b[0]);

    f1(i,n) {
        if (arr_a[i] == arr_a[i-1]) {
            longest_a++;
        } else {
            longest_a = 1;
        }

        if (arr_b[i] == arr_b[i-1]) {
            longest_b++;
        } else {
            longest_b = 1;
        }

        count_a_map[arr_a[i]] = max(count_a_map[arr_a[i]], longest_a);
        count_b_map[arr_b[i]] = max(count_b_map[arr_b[i]], longest_b);


        unique_elements.insert(arr_a[i]);
        unique_elements.insert(arr_b[i]);
    }



    ll result = 0;
    for (const int& element : unique_elements) {
        int count_a = count_a_map[element];
        int count_b = count_b_map[element];
        result = max(result, (ll)count_a + (ll)count_b);
    }
    cout << result << endl;
}

